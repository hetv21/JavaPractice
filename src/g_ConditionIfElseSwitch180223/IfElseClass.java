package g_ConditionIfElseSwitch180223;
    public class IfElseClass {
    public static void main(String[] args) {
        // which number is bigger
        int a= 30;
        int b= 20;
        int c= a+b;  // c= 50
        int d= a*b; // d= 600
        int e= a-b; // e= 10
        if (c>d && c>e){
            System.out.println(c); }
        else {
            System.out.println(d); }
    }
}
